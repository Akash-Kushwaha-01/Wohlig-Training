const mongoose = require('mongoose');

// Define a Mongoose Schema
const personSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    age: {
        type: Number,
    },
    work: {
        type: String,
        enum: ['chef', 'waiter', 'manager']
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    address: {
        type: String,
    },
    salary: {
        type: Number,
        required: true
    }
});

// Create Person Model
const Person = mongoose.model('Person', personSchema);

// Export the model
module.exports = Person;
